    public void getDanhMucCB() {
        danhMucCB.removeAllItems();

        try {
            danhMucs = DanhMucDAO.getAll();
            for (DanhMuc danhMuc : danhMucs) {
                danhMucCB.addItem(danhMuc.getName());
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        danhMucCB.setSelectedIndex(-1);
    }
    
    public void getNganSachCB() {
        nganSachCB.removeAllItems();

        try {
            nganSachs = NganSachDAO.getAll();
            for (NganSach nganSach : nganSachs) {
                nganSachCB.addItem(nganSach.getName());
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        nganSachCB.setSelectedIndex(-1);
    }
    
    private void setValueForAllFields() {
        idTF.setText(Integer.toString(gd.id));
        moneyTF.setText(Double.toString(gd.money));
        createdDayTF.setDate(gd.createdDay);
        noteTF.setText(gd.note);
        
        for (int i = 0; i < nganSachs.size(); i++) {
            if (nganSachs.get(i).getID() == gd.nganSachID) {
                nganSachCB.setSelectedIndex(i);
                break;
            }
        }
        
        for (int i = 0; i < danhMucs.size(); i++) {
            if (danhMucs.get(i).getID() == gd.danhMucID) {
                danhMucCB.setSelectedIndex(i);
                break;
            }
        }
    }

