    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        String money = moneyTF.getText();
        String id = idTF.getText();
        String note = noteTF.getText();
        int selectedDanhMuc = danhMucCB.getSelectedIndex();
        int selectedNganSach = nganSachCB.getSelectedIndex();
        
        double moneyValue;
        
        // Kiem tra
        if (money.isEmpty()) {
            JOptionPane.showMessageDialog(getContentPane(), "Vui lòng nhập số tiền.");
            return;
        }
         
        try {
           moneyValue = Double.parseDouble(money);
           
           if (moneyValue < 0) {
                moneyValue = -moneyValue;
            }
            
            if (this.gd.money <= 0) {
                moneyValue = -moneyValue;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(getContentPane(), "Vui lòng nhập đúng định dạng của số tiền.");
            return;
        }

        if (createdDayTF.getDate() == null) {
            JOptionPane.showMessageDialog(getContentPane(), "Vui lòng chọn ngày.");
            return;
        }
        
        if (selectedDanhMuc == -1 && gd.money <= 0) {
            JOptionPane.showMessageDialog(getContentPane(), "Vui lòng chọn danh mục.");
            return;
        }
   
        // Hien thi thong bao xac nhan
        int response = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn sửa không?", "Xác Nhận", JOptionPane.YES_NO_OPTION);
        if (response == 1) {
            return;
        }
        
        java.sql.Date date;
        date = new java.sql.Date(createdDayTF.getDate().getTime());
        
        try {
           GiaoDich instance = new GiaoDich();
           instance.id = Integer.parseInt(id);
           
           if (selectedDanhMuc != -1) {
               instance.danhMucID = danhMucs.get(selectedDanhMuc).getID();
           } else {
               instance.danhMucID = 0;
           }
           
           instance.nganSachID = nganSachs.get(selectedNganSach).getID();
           instance.note = note;
           instance.createdDay = date;
           instance.money = moneyValue;
           GiaoDichDAO.update(instance);
           
           JOptionPane.showMessageDialog(getContentPane(), "Sửa thành công");
           this.coordinator.stop();
       } catch (SQLException e) {
           e.printStackTrace();
       }
    }//GEN-LAST:event_updateButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int response = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xoá không?", "Xác Nhận", JOptionPane.YES_NO_OPTION);
        if (response == 1) {
            return;
        }
        
        try {
            String id = idTF.getText();
            GiaoDichDAO.delete(Integer.parseInt(id));
            JOptionPane.showMessageDialog(getContentPane(), "Xoá thành công");
            this.coordinator.stop();
        } catch (SQLException ex) {
            Logger.getLogger(UpdateGiaoDichView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

